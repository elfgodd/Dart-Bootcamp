void main() {
  
  // switch statement
  
  int grade = 11;
  
  switch(grade) {
    case 10:
      print('Excellent');
      break;
    case 9:
      print('Very good');
      break;
    case 8:
      print('Good');
      break;
    case 7:
      print('Regular');
      break;
    case 6:
      print('Enough');
      break;
    default:
      print('You didn\'t pass');
      break;
  }
}

